type WidenLiterals<T> =
  T extends string ? string
  : T extends number ? number
  : T extends boolean ? boolean
  : T extends (...args: infer Args) => infer Return ? (...args: Args) => Return
  : T extends Array<infer Item> ? Array<WidenLiterals<Item>>
  : T extends object ? { [Key in keyof T]: WidenLiterals<T[Key]> }
  : T

export type AppMessages = WidenLiterals<typeof messages.en>

export const UiMessagesContext = createContext<AppMessages>(messages.en)

export function useUiMessages() {
  return useContext(UiMessagesContext)
}
